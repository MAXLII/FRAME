@dataclass
class I18nManager:
    language: str = "zh"

    def __post_init__(self) -> None:
        self._alias_to_key: dict[str, str] = {}
        self._pattern_entries: list[tuple[re.Pattern[str], str, list[str]]] = []
        for key, values in TRANSLATIONS.items():
            self._alias_to_key[key] = key
            for translated in values.values():
                self._alias_to_key[translated] = key
            if "{" in key and "}" in key:
                raw_fields = re.findall(r"\{([^}]+)\}", key)
                fields: list[str] = []
                for raw_field in raw_fields:
                    field_name = raw_field.split(":", 1)[0]
                    fields.append(field_name)
                templates = [key, *values.values()]
                for template in templates:
                    pattern_text = re.escape(template)
                    for raw_field in raw_fields:
                        field_name = raw_field.split(":", 1)[0]
                        pattern_text = pattern_text.replace(r"\{" + raw_field + r"\}", f"(?P<{field_name}>.+?)")
                    self._pattern_entries.append((re.compile(f"^{pattern_text}$"), key, fields))

    def set_language(self, language: str) -> None:
        self.language = "en" if language == "en" else "zh"

    def get_language_labels(self) -> tuple[str, ...]:
        return tuple(LANGUAGE_DISPLAY[code] for code in ("zh", "en"))

    def get_language_from_label(self, label: str) -> str:
        return "en" if label == LANGUAGE_DISPLAY["en"] else "zh"

    def get_label_for_language(self, language: str) -> str:
        return LANGUAGE_DISPLAY["en" if language == "en" else "zh"]

    def translate_text(self, text: str) -> str:
        key = self._alias_to_key.get(text)
        if key is not None:
            return TRANSLATIONS[key].get(self.language, text)
        for pattern, template_key, fields in self._pattern_entries:
            match = pattern.match(text)
            if match:
                translated_template = TRANSLATIONS[template_key].get(self.language, template_key)
                values = {field: match.group(field) for field in fields}
                return translated_template.format(**values)
        return text

    def format_text(self, template: str, **kwargs) -> str:
        return self.translate_text(template).format(**kwargs)
